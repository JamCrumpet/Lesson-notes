# sorted() is used to sort data without affecting the actual order of the list
cars =["bmw", "audi", "toyota", "sabaru"]
print("Here is the original list:")
print(cars)
print("\nHere is the sorted list:")
print(sorted(cars)) # cars is sorted but only for this list
print("\nHere is the original list again:")
print(cars) # cars is reverted back to original order