# Difference between for and while loops

# for loop:
# executes a block of code once for each item in the collection

# while loops:
# runs as long as, or while, a certain condition is true

current_number = 1 # start with number 1
while current_number <= 5:
    print(current_number)
    current_number += 1 # code inside while loop keeps adding one to current_number

# python repeats the loop until current_number is greater than 5

