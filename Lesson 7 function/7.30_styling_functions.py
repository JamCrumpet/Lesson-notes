# you need to keep a few details in mind when you're styling functions
# functions should have descriptive names and names should used use lowercase letters and underscores

# descriptive names allow you and others to understand what your code is trying to do

# every function should have a comment that explains concisely what the function does

# if you specify a default value fro a parameter not spaces should be used either side of the equals sign
# for example
# def function_name(parameter_0, parameter_1="default value")

# the same convention should be used for keyword arguments in function calls
# for example
# function_name(value_0, parameter_1="value"

# most editors automatically line up any additional lines of parameter to match indentation you established in 1st line
# for example
# def function_name(
#         parameter_0, parameter_1, parameter_2,
#         parameter_3, parameter_4, parameter_5)

# if your program or moudle has more than one function you can separate each by two blank lines to make it easier ...
# ... to see where one function ends and one begins

# all import statements should be written at the beginning of a file
# the only exception is if you use comment at hte beginning of the file to describe the overall program
