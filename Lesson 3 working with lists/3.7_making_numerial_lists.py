# the range() function can be used to generate a series of numbers
for x in range(1, 5):
    print(x)

# although it looks like it should print numbers 1 to 5 it only prints 1 to 4
# this is because range() starts counting from the first value

# to print numbers 1 to 5 you could type
for y in range(1, 6):
    print(y)
