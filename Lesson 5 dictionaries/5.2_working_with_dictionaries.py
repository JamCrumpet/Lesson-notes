# a dictionary is a collection of key value pairs
# each key is connected to a value
# you can use a key to access the value associated with that key
# a key can be any object that you can create in python asa value in a dictionary i.e. number, string, list, dictionary

# in python a key is wrapped in brace {}, with a series of key-value pairs inside the braces. For example:

alien_0 = {"colour": "green", "points": 5}
# colour is associated to green via colon and separated from points via comma

# a key value pair is a set of values associated with each other
# when you provide a key python returns the value associated with the key
# every key is connected to its value by a colon, and individual key values are separated by commas
# you can store as many as many key pairs as you want in a dictionary

alien_1 = {"colour" : "yellow"}

# the simplest dictionary stores on piece of about alien_1, namely it's colour
