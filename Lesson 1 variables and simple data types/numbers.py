# Python calls any number with a decimal a float
# >>> 0.1 + 0.1
# 2
# >>> 0.2 + 0.2
# 0.4
# >>> 2 * 0.1
# 0.2
# >>> 2 * 0.2
# 0.4

# Sometimes an arbitrary number of decimals places can be be in the answer
# >>> 0.2 + 0.1
# 0.2 + 0.1
# 0.30000000000000004
# >>> 3 * 0.1
# 0.30000000000000004
# This is due to the difficulty of defining decimals in binary so an approximate answer is given